static void SystemClock_Config( void );

/* Private functions ---------------------------------------------------------*/

/**
  * @brief  Main program.
  * @param  None
  * @retval None
  */
int main( void )
{
    /* STM32L0xx HAL library initialization:
         - Configure the Flash prefetch, Flash preread and Buffer caches
         - Systick timer is configured by default as source of time base, but user
               can eventually implement his proper time base source (a general purpose
               timer for example or other time source), keeping in mind that Time base
               duration should be kept 1ms since PPP_TIMEOUT_VALUEs are defined and
               handled in milliseconds basis.
         - Low Level Initialization
       */
    HAL_Init();

    /* Configure the System clock to 2 MHz */
    SystemClock_Config();

    /* Enables the Ultra Low Power mode */
    HAL_PWREx_EnableUltraLowPower( );

    /* Initialize LED */
    BSP_LED_Init( LED2 );

    osThreadDef( Thread, ToggleLedThread, osPriorityBelowNormal, 0, configMINIMAL_STACK_SIZE );
    osThreadCreate( osThread( Thread ), NULL );

    /* Start scheduler */
    osKernelStart();

    /* We should never get here as control is now taken by the scheduler */
    for( ;; );

}

/**
  * @brief  Message Queue Producer Thread.
  * @param  argument: Not used
  * @retval None
  */
static void ToggleLedThread( const void *argument )
{
    for( ;; )
    {
        osDelay( LED_TOGGLE_DELAY );
        BSP_LED_Toggle( LED2 );
    }
}


void vMainPreStopProcessing( void )
{
    GPIO_ConfigAN();
}


void vMainPostStopProcessing( void )
{
    /* Enable the Internal High Speed oscillator (MSI). */
    __HAL_RCC_MSI_ENABLE();

    /* Selects the Multiple Speed oscillator (MSI) clock range .*/
    __HAL_RCC_MSI_RANGE_CONFIG( RCC_MSIRANGE_5 );
    /* Adjusts the Multiple Speed oscillator (MSI) calibration value.*/
    __HAL_RCC_MSI_CALIBRATIONVALUE_ADJUST( 0x00 );

    MODIFY_REG( RCC->CFGR, RCC_CFGR_SW, RCC_SYSCLKSOURCE_MSI );

    /* Update the SystemCoreClock global variable */
    SystemCoreClock =  HAL_RCC_GetSysClockFreq() >> AHBPrescTable[( ( RCC->CFGR & RCC_CFGR_HPRE ) >> 4 )];

    BSP_LED_Init( LED2 );

}

/**
  * @brief  Configure all GPIO's to AN to reduce the power consumption
  * @param  None
  * @retval None
  */
static void GPIO_ConfigAN( void )
{
    GPIO_InitTypeDef GPIO_InitStruct;

    /* Configure all GPIO as analog to reduce current consumption on non used IOs */
    /* Enable GPIOs clock */
    __HAL_RCC_GPIOA_CLK_ENABLE();
    __HAL_RCC_GPIOB_CLK_ENABLE();
    __HAL_RCC_GPIOC_CLK_ENABLE();
    __HAL_RCC_GPIOD_CLK_ENABLE();
    __HAL_RCC_GPIOH_CLK_ENABLE();

    GPIO_InitStruct.Mode = GPIO_MODE_ANALOG;
    GPIO_InitStruct.Pull = GPIO_NOPULL;
    GPIO_InitStruct.Pin = GPIO_PIN_All;
    HAL_GPIO_Init( GPIOA, &GPIO_InitStruct );
    HAL_GPIO_Init( GPIOB, &GPIO_InitStruct );
    HAL_GPIO_Init( GPIOC, &GPIO_InitStruct );
    HAL_GPIO_Init( GPIOD, &GPIO_InitStruct );
    HAL_GPIO_Init( GPIOH, &GPIO_InitStruct );

    /* Disable GPIOs clock */
    __HAL_RCC_GPIOA_CLK_DISABLE();
    __HAL_RCC_GPIOB_CLK_DISABLE();
    __HAL_RCC_GPIOC_CLK_DISABLE();
    __HAL_RCC_GPIOD_CLK_DISABLE();
    __HAL_RCC_GPIOH_CLK_DISABLE();
}

/**
  * @brief  System Clock Configuration
  *         The system Clock is configured as follow :
  *            System Clock source            = MSI
  *            SYSCLK(Hz)                     = 2000000
  *            HCLK(Hz)                       = 2000000
  *            AHB Prescaler                  = 1
  *            APB1 Prescaler                 = 1
  *            APB2 Prescaler                 = 1
  *            Flash Latency(WS)              = 0
  *            Main regulator output voltage  = Scale3 mode
  * @param  None
  * @retval None
  */
static void SystemClock_Config( void )
{
    RCC_ClkInitTypeDef RCC_ClkInitStruct;
    RCC_OscInitTypeDef RCC_OscInitStruct;

    /* Enable Power Control clock */
    __HAL_RCC_PWR_CLK_ENABLE();

    /* The voltage scaling allows optimizing the power consumption when the device is
       clocked below the maximum system frequency, to update the voltage scaling value
       regarding system frequency refer to product datasheet.  */
    __HAL_PWR_VOLTAGESCALING_CONFIG( PWR_REGULATOR_VOLTAGE_SCALE3 );

    /* Enable MSI Oscillator */
    RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_MSI;
    RCC_OscInitStruct.MSIState = RCC_MSI_ON;
    RCC_OscInitStruct.MSIClockRange = RCC_MSIRANGE_5;
    RCC_OscInitStruct.MSICalibrationValue = 0x00;
    RCC_OscInitStruct.PLL.PLLState = RCC_PLL_NONE;
    HAL_RCC_OscConfig( &RCC_OscInitStruct );


    /* Select MSI as system clock source and configure the HCLK, PCLK1 and PCLK2
       clocks dividers */
    RCC_ClkInitStruct.ClockType = ( RCC_CLOCKTYPE_SYSCLK | RCC_CLOCKTYPE_HCLK | RCC_CLOCKTYPE_PCLK1 | RCC_CLOCKTYPE_PCLK2 );
    RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_MSI;
    RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
    RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV1;
    RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV1;
    HAL_RCC_ClockConfig( &RCC_ClkInitStruct, FLASH_LATENCY_0 );

}


